export function getJobById(id) {
  return jobs.find((job) => job.id === id);
}

export function getJobsByFilters({ category, trade, search }) {
  return jobs.filter((job) => {
    if (category && category !== "All" && job.category !== category) return false;
    if (trade && trade !== "All" && job.trade !== trade) return false;
    if (search) {
      const q = search.toLowerCase();
      return (
        job.role.toLowerCase().includes(q) ||
        job.country.toLowerCase().includes(q) ||
        job.trade.toLowerCase().includes(q)
      );
    }
    return true;
  });
}